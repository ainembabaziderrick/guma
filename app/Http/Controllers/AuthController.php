    /**
     * Store a newly created user in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'name' => 'required|string|max:255',
            'email' => 'required|string|email|max:255|unique:users',
            'phone' => 'required|string|max:20',
            'sex' => 'required|in:Male,Female',
            'identification_type' => 'required|in:NIN,Passport',
            'nin' => 'required_if:identification_type,NIN|nullable|string|max:20',
            'passport' => 'required_if:identification_type,Passport|nullable|string|max:20',
            'status' => 'required|in:Experienced,Non Experienced',
            'password' => 'required|string|min:8|confirmed',
        ]);

        if ($validator->fails()) {
            return redirect()->back()
                        ->withErrors($validator)
                        ->withInput();
        }

        $user = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'phone' => $request->phone,
            'sex' => $request->sex,
            'identification_type' => $request->identification_type,
            'nin' => $request->identification_type === 'NIN' ? $request->nin : null,
            'passport' => $request->identification_type === 'Passport' ? $request->passport : null,
            'status' => $request->status,
            'level' => 3, 
            'password' => Hash::make($request->password),
        ]);

        event(new Registered($user));

        auth()->login($user);

        return redirect()->intended('/dashboard')->with('success', 'Registration successful!');
    }

